var changeColor = document.getElementById("col");

function colorcyan() {
        changeColor.style.backgroundColor = 'cyan'
}

function colormagenta() {
        changeColor.style.backgroundColor = 'Magenta'
}

function coloryellow() {
        changeColor.style.backgroundColor = 'Yellow'
}

function colorblack() {
        changeColor.style.backgroundColor = 'Black' 
}

function clearColor() {
        changeColor.removeAttribute('style');
}